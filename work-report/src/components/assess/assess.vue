        <span style="color: transparent">集团</span>（4）其它个性系统做得较好的有：物资供应分公司的价格管控系统，首次将价格管理纳入信息化范畴；东大化工公司健全安全环保预警装置，加强污染物排放的实时监测；尼龙化工公司完善设备资产管理系统，完善设备台账管理，强化检修计划、计量及专用设备管理；土建处的二维码施工管理系统，通过手机扫码即可实现人员或设备的相关信息共享；二矿的井下新一代无线WIFI通信，实现井上、下覆盖范围内移动终端之间、手机与调度电话及无线摄像机等监控设备的互联互通；十一矿的智能充电柜，提高了设备的工作效率，实现了减人提效；五矿的智能选矸系统，实现了原煤和矸石的自动分离。
      </div>
      <div>
        <span style="color: transparent">集团</span>（5）系统集成水平：矿井综合自动化系统集成大多处于界面级集成，涉及人、财、物等经营管理的系统集成水平较差，主要原因在于：一是编码不统一；二是不同开发商的系统之间对接难度大，信息孤岛现象仍很严重。
      </div>
      <div>
        三、测评建议
      </div>
      <div>
        <span style="color: transparent">集团</span>1、整体规划，分步实施
      </div>
      <div>
        <span style="color: transparent">集团</span>集团和各单位的重大信息化项目实施前要做好整体规划，并对规划多方论证，保证其科学性、先进性、实用性和可实施性，根据能力进行分步实施，保证项目的综合质量和延续性。
      </div>
      <div>
        <span style="color: transparent">集团</span>2、扎实推进，注重实效
      </div>
      <div>
        <span style="color: transparent">集团</span>随着集团各板块业务的不断拓展，急需一套集网上资源共享、招投标于一身的电商平台，承担全集团各单位采购、销售、招标和回收复用等电子商务工作，在推进过程中应充分收集各业务板块的需求，注重实效，打造成为具有平煤神马特色的电子商务平台。
      </div>
      <div>
        <span style="color: transparent">集团</span>3、打造示范，推进建设
      </div>
      <div>
        <span style="color: transparent">集团</span>2017年，主编发布了《河南省智能煤矿建设规范》，出台了《集团智慧选煤厂建设标准》（1.0）、《集团智能矿山建设标准》（2.0）。集团各相关单位要以两个标准的实施为契机，做好智能矿山和智慧选煤厂的建设推进工作，打造“智能矿山”和“智慧选煤厂”的示范单位。同时，积极开展《智能工厂（化工）建设规范》的编写，提升集团智能工厂的建设。
      </div>
      <div>
        <span style="color: transparent">集团</span>4、搞好培训，培养人才
      </div>
      <div>
        <span style="color: transparent">集团</span>加大培训力度，提升培训内容，落实具体措施。针对人员流动性大的问题，提升专业化的培训水平，做到培训与岗位需求相结合、培训与技术发展相结合、培训与安全生产相结合、培训与技能鉴定相结合、培训与生产管理相结合，从而针对性的为信息化领域培养出专业人才。
      </div>
      <div>
        <span style="color: transparent">集团</span>5、加强信安，重视防范
      </div>
      <div>
        <span style="color: transparent">集团</span>工控安全已得到国家的充分重视，在强化《集团信息安全管理办法》落实的同时，继续推进工控安全在井上下和地面厂的应用，出台《集团工控安全管理办法》，为两化深度融合做好信息安全保障。
      </div>
      <div>
        <span style="color: transparent">集团</span>6、技术创新，提升竞争
      </div>
      <div>
        <span style="color: transparent">集团</span>技术创新是企业持续发展的根本保障。集团应重点关注移动信息化、物联网、云计算、大数据、互联网+、人工智能等新技术与实体经济的融合，对发展过程中的关键环节、主要措施和主要标准加以规范引导，推进集团智能煤矿和智慧工厂的建设，提升集团核心竞争力。
      </div>
      <div>
        四、	具体意见归集
      </div>
      <div>
        <span style="color: transparent">集团</span>1、加大两化融合应用技术的培训力度，提升应用水平。
      </div>
      <div>
        <span style="color: transparent">集团</span>健全激励机制，加强培训，做好人才储备。开展多层次专业知识培训工作，做好定期培训计划和专项培训计划，使信息化队伍能够持续不断的进行专业知识的提升；以项目为主建立各单位之间信息化人才培养和资源共享机制；组织专家到基层单位进行信息化交流和讲座，努力提升信息化人才的专业技术业务能力。
      </div>
      <div>
        <span style="color: transparent">集团</span>2、创新技术交流形式。建议采取集团信息办为主导，在信息化示范单位设立管理、维护、研发等方面实习基地，有针对性的为信息化的一线员工提供实习机会，进而为人才少的单位培养信息化专业人才。
      </div>
      <div>
        <span style="color: transparent">集团</span>3、加大信息化资金投入。把信息化作为主要工具和手段，结合煤炭生产和经营管理需要，以云计算、大数据、物联网、移动信息化等技术手段为核心，围绕煤炭生产信息化、装备数字化、决策智能化的目标，加大信息化资金投入力度，谋划未来、抓出实效，推进“两化深度融合”。
      </div>
      <div>
        <span style="color: transparent">集团</span>4、强化专业应用系统的建设要求，能够为后期智能化进行数据提取，将其成功的经验和技术进行交流和推广。
      </div>
      <div>
        <span style="color: transparent">集团</span>5、集团财务系统统一部署。由于财务系统运行时间较长，应用过程中出现的问题比较多，各单位对升级财务系统的需求都很迫切，因此希望集团统一负责财务系统的升级问题。
      </div>
